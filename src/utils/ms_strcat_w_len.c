void	ms_strcat_w_len(char *dest, char *source, int l1, int l2)
{
	int		i;

	i = 0;
	while (i < l2)
	{
		dest[l1 + i] = source[i];
		i++;
	}
	dest[l1 + l2] = '\0';
}
